from pulp import LpProblem, LpVariable, LpMaximize, value

# –°–æ–∑–¥–∞–µ–º –º–æ–¥–µ–ª—å –¥–ª—è –∑–∞–¥–∞—á–∏ –ª–∏–Ω–µ–π–Ω–æ–≥–æ –ø—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏—è —Å –Ω–∞–∑–≤–∞–Ω–∏–µ–º "Beverage_production" –∏ —Ü–µ–ª—å—é –º–∞–∫—Å–∏–º–∏–∑–∞—Ü–∏–∏ –ø—Ä–æ–∏–∑–≤–æ–¥—Å—Ç–≤–∞.
model = LpProblem(name="Beverage_production", sense=LpMaximize)

# –û–ø—Ä–µ–¥–µ–ª—è–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ —Ä–µ—à–µ–Ω–∏—è –¥–ª—è –ø—Ä–æ–∏–∑–≤–æ–¥—Å—Ç–≤–∞ –ª–∏–º–æ–Ω–∞–¥–∞ (A) –∏ —Ñ—Ä—É–∫—Ç–æ–≤–æ–≥–æ —Å–æ–∫–∞ (B), –æ–±–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –æ–≥—Ä–∞–Ω–∏—á–µ–Ω—ã —Å–Ω–∏–∑—É –Ω—É–ª–µ–º –∏ —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω–æ–≥–æ —Ç–∏–ø–∞.
lemonade = LpVariable(name="lemonade", lowBound=0, cat='Integer')
fruit_juice = LpVariable(name="fruit_juice", lowBound=0, cat='Integer')

# –ó–∞–¥–∞–µ–º —Ü–µ–ª–µ–≤—É—é —Ñ—É–Ω–∫—Ü–∏—é –¥–ª—è –º–∞–∫—Å–∏–º–∏–∑–∞—Ü–∏–∏ –æ–±—â–µ–≥–æ –ø—Ä–æ–∏–∑–≤–æ–¥—Å—Ç–≤–∞ –ª–∏–º–æ–Ω–∞–¥–∞ –∏ —Ñ—Ä—É–∫—Ç–æ–≤–æ–≥–æ —Å–æ–∫–∞.
model += lemonade + fruit_juice, 'Production'

# –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏—è –Ω–∞ –º–∞—Ç–µ—Ä–∏–∞–ª—ã: –≤–æ–¥–∞, —Å–∞—Ö–∞—Ä, –ª–∏–º–æ–Ω–Ω—ã–π —Å–æ–∫ –∏ —Ñ—Ä—É–∫—Ç–æ–≤–æ–µ –ø—é—Ä–µ.
model += 2 * lemonade + fruit_juice <= 100, 'Water'
model += lemonade <= 50, 'Sugar'
model += lemonade <= 30, 'Lemon juice'
model += 2 * fruit_juice <= 40, 'Fruit puree'

# –†–µ—à–∞–µ–º –∑–∞–¥–∞—á—É –ª–∏–Ω–µ–π–Ω–æ–≥–æ –ø—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏—è.
model.solve()

# –í—ã–≤–æ–¥–∏–º –æ–ø—Ç–∏–º–∞–ª—å–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è –ø—Ä–æ–∏–∑–≤–æ–¥—Å—Ç–≤–∞ –ª–∏–º–æ–Ω–∞–¥–∞ –∏ —Ñ—Ä—É–∫—Ç–æ–≤–æ–≥–æ —Å–æ–∫–∞, –∞ —Ç–∞–∫–∂–µ –æ–±—â–µ–µ –ø—Ä–æ–∏–∑–≤–æ–¥—Å—Ç–≤–æ.
print('\n')
print("üîÑ –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞ –∫—ñ–ª—å–∫—ñ—Å—Ç—å –ø—Ä–æ–¥—É–∫—Ç—É:", round(model.objective.value(), 2))
print("üçã –õ–µ–º–æ–Ω–∞–¥—É:", round(lemonade.value(), 2))
print("üçè –§—Ä—É–∫—Ç–æ–≤–æ–≥–æ —Å–æ–∫—É:", round(fruit_juice.value(), 2))
print('\n')
